public class Lab12A{
    public static void main(String[]args)
    {
        Pattern(5);
        }
        static void Pattern(int x){
            for(int i=1;i<=x;i++)
            {
                for(int j=1;j<=i;j++)
                {
                     System.out.print("*");
                }
             System.out.println();  
            }
        }
}
    